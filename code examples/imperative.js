//function for that will multiply numbers by two
function multiplyByTwo() {
    let value = [1, 2, 3, 4, 5]
    for (let i = 0; i < 5; i++){
        value[i] = value[i] * 2   
    }
    console.log(value);
}
multiplyByTwo(); 


